#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	int		sgn;
	size_t	res;

	i = 0;
	sgn = 1;
	res = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sgn = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + (str[i] - '0');
		i++;
	}
	if (res > 9223372036854775807)
		return (0 - (sgn == 1));
	return (res * sgn);
}
